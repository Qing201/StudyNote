# [206] Reverse Linked List
#

# @lc code=start
# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def reverseList(self, head: Optional[ListNode]) -> Optional[ListNode]:
        node = head
        pre = None
        while node != None:
            temp = node.next
            node.next = pre
            pre = node
            node = temp
        return pre
            

# @lc code=end

